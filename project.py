print("Hello, World!")

# Primitive Data Types
# String Example
name = "Miguel"
# Integer Example
age = 41
# Float Example
height = 1.77
# Boolean Example
is_student = False
# Print variable values
print(name)
print(age)
print(height)
print(is_student)

# Non-Primitive Data Types
# List Example
fruits = ["Apple", "Banana", "Orange"]
print("List Example:", fruits)

# Tuple Example
coordinates = (10, 20)
print("Tuple Example:", coordinates)

# Dictionary Example
person = {
  "name": "Miguel",
  "age": 41,
  "is_student": False,
}
print("Dictionary Example:", person)